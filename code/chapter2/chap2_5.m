# 行列

## 行列の入力
A = [1 2 -3; 1 0 -2; -1 0 1;];
A

# A =
#   1   2  -3
#   1   0  -2
#  -1   0   1

## 逆行列
M = inv(A)
# M =
#        0  -1.0000  -2.0000
#   0.5000  -1.0000  -0.5000
#        0  -1.0000  -1.0000

M*A
# ans =
#   1   0   0
#   0   1   0
#   0   0   1

A*M
# ans =
#   1   0   0
#   0   1   0
#   0   0   1


## 共役転置
A'
# ans =
#   1   1  -1
#   2   0   0
#  -3  -2   1

A2 = [1-i 2+2i; 1 0; -1i 0;];
# A2 =
#   1 - 1i   2 + 2i
#   1 + 0i   0 + 0i
#   0 - 1i   0 + 0i

A2'
# ans =
#   1 + 1i   1 - 0i   0 + 1i
#   2 - 2i   0 - 0i   0 - 0i

A2.'
# ans =
#   1 - 1i   1 + 0i   0 - 1i
#   2 + 2i   0 + 0i   0 + 0i
